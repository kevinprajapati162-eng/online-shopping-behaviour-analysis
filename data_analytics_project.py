def clean_likert_answer(text_value):
    """Map 'Extremely important' etc. to scores 1–5"""
    if pd.isna(text_value):
        return np.nan
    text = str(text_value).strip().lower()
    if "extremely important" in text:
        return 5
    if "very important" in text:
        return 4
    if "fairly important" in text:
        return 3
    if "slightly important" in text:
        return 2
    if "not important at all" in text or "not important" in text:
        return 1
    if "important" in text:
        return 4
    return np.nan

