from scipy.stats import chi2_contingency

gender_freq_table = pd.crosstab(shopping_data["gender"], shopping_data["frequent_shopper"])
chi2_val, p_val, dof, expected_table = chi2_contingency(gender_freq_table)

print("Gender vs frequent shopper contingency table:\n", gender_freq_table, "\n")
print("Expected counts under independence:\n", pd.DataFrame(expected_table,
      index=gender_freq_table.index, columns=gender_freq_table.columns))

print(f"\nChi-square statistic: {chi2_val:.3f}")
